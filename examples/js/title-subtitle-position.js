(function () {
    let env = window.muze();
    const DataModel = window.muze.DataModel;

    d3.json('/data/cars.json', (data) => {
        schema = [{
        name: 'Name',
        type: 'dimension'
    }, {
        name: 'Maker',
        type: 'dimension'
    }, {
        name: 'Miles_per_Gallon',
        type: 'measure'
    }, {
        name: 'Displacement',
        type: 'measure'
    }, {
        name: 'Horsepower',
        type: 'measure'
    }, {
        name: 'Weight_in_lbs',
        type: 'measure'
    }, {
        name: 'Acceleration',
        type: 'measure'
    }, {
        name: 'Origin',
        type: 'dimension'
    }, {
        name: 'Cylinders',
        type: 'dimension'
    }, {
        name: 'Year',
        type: 'dimension',
        subtype: 'temporal',
        format: '%Y-%m-%d'
    }];

    const rootData = new DataModel(data, schema);
    const canvas = env.canvas();
    
    canvas
        .columns(['Acceleration'])
        .rows(['Cylinders'])
        .data(rootData)
        .height(400)
        .width(400)
        .title('The car', { position: 'bottom', align: 'center' })
        .subtitle('The car', { position: 'bottom', align: 'center' })
        .mount('#chart');
    });
}());
